public class HOBBY {
    String nombre;
    String dia;

    public HOBBY (String nombre, String dia){
        this.nombre=nombre;
        this.dia=dia;
    }

    public String getNombre() {
        return nombre;
    }

    public String getDia() {
        return dia;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }
}
